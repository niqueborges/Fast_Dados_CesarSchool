# Soma Simples - Leia dois valores inteiros, no caso para variáveis a e b. A seguir, calcule a soma entre elas e atribua à variável soma. A seguir, exiba o valor desta variável.


a = int(input('Digite o primeiro número: '))
b = int(input('Digite o segundo número: '))

soma = a + b

print(f'A soma de {a} e {b} é {soma}')